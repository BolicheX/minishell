#include "../libft.h"

int	ft_isnum(char *s)
{
	if (*s == '\0')
		return (0);
	if (*s == '-' || *s == '+')
		s++;
	if (ft_isdigit(*s))
	{
		while (*s)
		{
			if (!ft_isdigit(*s))
				return (0);
			s++;
		}
		return (1);
	}
	return (0);
}
